
public class Calculator {
	public int sum (int a,int b) {
	return a+b;
	}//sum
	
	
	public int divide (int a,int b) {
		return a/b;
		}//divide
	
	public double getPI() {
		return 3.1415926;
		}//getPI
	
	public String getStrPi() {
		return Double.toString(getPI());
	}//getStrPi
}//class Calculator
